    private void saludar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saludar
        // TODO add your handling code here:
        String nombreEscrito = this.textFieldNombre.getText();
        if(nombreEscrito==null || nombreEscrito.trim().length()==0){
            JOptionPane.showMessageDialog(this, "Debe escribir primero un nombre", "Aviso" ,JOptionPane.WARNING_MESSAGE );
            
        }
        else{
            JOptionPane.showMessageDialog(this, "Hola " + nombreEscrito, "Saludo" ,JOptionPane.INFORMATION_MESSAGE );
        }
    }//GEN-LAST:event_saludar

